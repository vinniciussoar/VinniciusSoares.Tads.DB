CREATE TABLE DEPARTAMENTO (
    DEPARTAMENTO_ID NUMBER GENERATED BY DEFAULTON NULL AS IDENTITY PRIMARY KEY,
    NOME VARCHAR (100) NOT NULL
);

CREATE TABLE GERENTE (
    GERENTE_ID NUMBER GENERATED BY DEFAULTON NULL AS IDENTITY PRIMARY KEY,
    NOME VARCHAR (100) NOT NULL,
    DEPARTAMENTO_ID NUMBER NOT NULL,
    CONSTRAINT FK_GERENTE_DEPARTAMENTO FOREIGN KEY (DEPARTAMENTO_ID) REFERENCES DEPARTAMENTO (DEPARTAMENTO_ID)
);

CREATE TABLE FUNCIONARIO (
    FUNC_ID NUMBER GENERATED BY DEFAULTON NULL AS IDENTITY PRIMARY KEY,
    NOME VARCHAR (100) NOT NULL,
    GERENTE_ID NUMBER NOT NULL,
    CONSTRAINT FK_FUNCIONARIO_GERENTE FOREIGN KEY (GERENTE_ID) REFERENCES GERENTE (GERENTE_ID)
);