CREATE TABLE ALUNOS (
    id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    nome VARCHAR2(100) NOT NULL
);


CREATE TABLE HOBBIES (
    id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    descricao VARCHAR2(100) NOT NULL UNIQUE
);


CREATE TABLE ALUNO_HOBBY (
    aluno_id NUMBER NOT NULL,
    hobby_id NUMBER NOT NULL,
    
    
    CONSTRAINT fk_aluno FOREIGN KEY (aluno_id) REFERENCES ALUNOS(id),
    CONSTRAINT fk_hobby FOREIGN KEY (hobby_id) REFERENCES HOBBIES(id),
    
    
    PRIMARY KEY (aluno_id, hobby_id)
);
